#import "UIColor+Additions.h"

@implementation UIColor (Additions)

+ (UIColor *)dblUpOrangeColor
{
    return [self colorWithRed:0.953 green:0.420 blue:0.337 alpha:1];
}

+ (UIColor *)dblUpRedColor
{
    return [self colorWithRed:0.902 green:0.000 blue:0.090 alpha:1];
}

+ (UIColor *)dblUpBlueColor
{
    return [self colorWithRed:0.392 green:0.682 blue:0.929 alpha:1];
}

+ (UIColor *)dblUpBlackColor
{
    return [self colorWithRed:0.200 green:0.200 blue:0.200 alpha:1];
}

+ (UIColor *)dblUpLightGreyColor
{
    return [self colorWithRed:0.702 green:0.702 blue:0.702 alpha:1];
}

+ (UIColor *)dblUpDarkGreyColor
{
    return [self colorWithRed:0.502 green:0.502 blue:0.502 alpha:1];
}

@end
